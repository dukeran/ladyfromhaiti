<h1 class="reddrop">GONE IN A FLASH-- AN ARTIST'S SINGULARLY SAD TALE</h1>
<div style="color: #FF0000;font-family:Arial, Helvetica, sans-serif;">by Melody Bales, owner of The Lady From Haiti Boutique</div>
 

<p><b style="color:blue;">About ten days after the terrible 2010 earthquake in Haiti that robbed more than 250,000 people of their lives, I got a phone call from Monsieur Clark Constant, a Haitian painter of "The Artibonite School" of Haitian paintings. He was in Ft. Lauderdale taking some classes at the time of the earthquake, away from his homeland and his family.<br/><br/>

Clark had seen "BREAKING NEWS: EARTHQUAKE IN HAITI" on CNN, simultaneously with the rest of us Americans. Dreading the answer, when Clark called me I fearfully forced myself to ask the question of "how his family had fared..." What a horrible question. Yet his heartbreaking reply was so much worse-- Over the phone came the unbelievable words, "They're all dead, Madame Melody, five of them from three generations, were crushed to death under a falling wall, including my new baby. ALL GONE."<br/><br/>

How can ONE human being help another human being under such practically incredible circumstances??? Well, actually it  "took a VILLAGE." After catching my breath and regaining the ability to reply,  I let Clark Constant know that we here in Naples, both residents and visitors, were heart-broken about the Haitians' plight and that we would do everything possible to help him.<br/><br/>

Very soon thereafter the generous people of Naples came to Clark's aid when I put out an "SOS" of sorts... Our city and its visitors raised over $800 in two weeks when I set an industrial size pickle jar on a chair outside my store, as a collection receptacle. Beside it I placed a very colorful and happy 8x10 scene that Clark had painted during the previous year when life for Haitians was not QUITE as bad as it was soon to become. Next to the painting leaned a sign revealing the sad news about Clark's family-- five killed in an instant when the "tremblement de terre" or earthquake began. The little sign said that the artist of that cheerful painting desperately needed the money to get back into Haiti via the Dominican Republic. Grief-stricken, helpless and panicked, Clark did know he could not save his family, but he had kept repeating to me over and over, "I need to go and help my country, Madame Melody, please!! PLEASE help me! My country needs me. I must go there and help them!"<br/><br/>

We all pulled together and within two weeks Clark was able to get an alternate flight into the Dominican Republic;  he then took days long truck rides to arrive back in Haiti, his home--  what was left of it.<br/><br/>

Three years later, on the first time I beheld Clark's face again, I didn't even recognize him. He was gaunt and appeared to have a haunted look: sunken cheeks and deep-set, wild, almost scared eyes. I suppose he WAS haunted in a way. He just may possibly be that way for the rest of his life. And there I firmly believe, but for the GRACE of God, go we all. Clark's mounds of unstreched painted canvasses, with him when he arrived that day unannounced, took me by surprise. Not just Clark's face, but also his huge efforts touched me greatly.<br/><br/>

The goal of this informal CYBER SIDEWALK PAINTINGS SALE is to gain back a portion of what I gave Clark on that day-- given simply as the only way I knew of to show him human kindness in his tragically altered life. Clark Constant is his family's sole earthquake survivor, yet he still finds within his soul the capacity to bring forth these lovely, LIVEly village scenes. You can benefit, too, by the current greatly reduced paintings prices. Grace your walls or gift your friends with these very special creations and please know that Clark and I thank you for buying his art.</b></p>

